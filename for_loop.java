public class for_loop {
    public static void main(String[] args) {
        // Statement 1 : set a variable before the loop starts
        // Statement 2 : Condition for a loop to Run
        // Statement 3 : Increment or Decrement the variable
        for (int i = 5 ; i > 0 ; i--){
            System.out.println(i);
        }
    }
}

